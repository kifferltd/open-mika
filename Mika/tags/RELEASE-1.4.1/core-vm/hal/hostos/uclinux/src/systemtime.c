#include "wonkatime.h"
#include <sys/time.h>

w_long getNativeSystemTime(void) {
  struct timeval now;
  w_ulong result;
  
  gettimeofday(&now, NULL);
  result = ((w_ulong)now.tv_sec * 1000ULL) + ((w_ulong)now.tv_usec / 1000ULL);
  return (w_long)result;
}

